import turtle
t=turtle.Turtle()
#t2=turtle.Turtle()
turtle.Screen().bgcolor("black")
t.pencolor("red")
t.pensize(2)
t.penup()
t.goto(-200,-50)
t.down()
t.fillcolor("yellow")
t.begin_fill()
t.left(60)
t.fd(400)
t.rt(120)
t.fd(400)
t.rt(120)
t.fd(400)
t.penup()

t.goto(200,180)
t.pendown()
t.fd(400)
t.lt(120)
t.fd(400)
t.lt(120)
t.fd(400)
t.end_fill()
t.ht()

turtle.Screen().exitonclick()